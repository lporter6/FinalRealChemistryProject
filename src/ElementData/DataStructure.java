package ElementData;

public class DataStructure{
	
	public String label;
	public double molarMass;
	
	public DataStructure(String label, double molarMass){
		
		this.label = label; //sets the object string label to the parameter in the constructor
		this.molarMass = molarMass; //sets the object double molar mass to the parameter in the constructor
		
	}
	
}